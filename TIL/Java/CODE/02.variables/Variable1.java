package chapter2;

public class Variable1 {

	public static void main(String[] args) {
		int level; // 정수형 변수 level을 선언 
		level = 10; // level 변수에 값 10을 대입
		System.out.println(level); // level값 출력
		
		int age;
		age = 28;
		System.out.println(age);
		
		int year = 2018;
		System.out.println(year);
	}

}

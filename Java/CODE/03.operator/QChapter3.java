package operator;

public class QChapter3 {

	public static void main(String[] args) {
		int num1;
		num1 = -5 + 3 * 10 / 2;
		System.out.println(num1);
		
		int num2 = 10;
		System.out.println(num2); //10
		System.out.println(num2++); //10
		System.out.println(num2); //11
		System.out.println(--num2); //10
		
	
	}

}
